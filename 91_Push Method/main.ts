// Question 91: Create an array of your three favorite fruits and add a new fruit to the end of the array ?

// Explain & TIP: Adding elements to an array can be easily done using the .push() method.
// It allows you to add new items to the end of an array.

// Answer 91:

let fruit_list = ["Mango","Apple","Pomegranate"];

console.log(`\nOriginal list: ${fruit_list}`);

fruit_list.push("Orange");

// Adding Orange in the end of array:
console.log(`\nUpdated List: ${fruit_list}`);