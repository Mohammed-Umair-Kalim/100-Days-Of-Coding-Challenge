// Question 87: Extracting a Substring: Demonstrate how to extract the first 10 characters from a string.

// Explain & TIP: .substring() can be used to get a specific part of a string, by specifying the start and
// end positions

// Answer 87:

let alphabets = "abcdefghijklmonpqrstuvwxyz";
//It will display first 10 characters from given string:
console.log("\n",alphabets.substring(0,10));

let digits = "01234567891011121314151617181920";
//It will display first 10 characters from given digits:
console.log("\n",digits.substring(0,10));